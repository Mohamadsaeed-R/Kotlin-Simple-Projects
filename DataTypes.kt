package com.example.project1
/*Defining variables in kotlin :
 var name :DataTypes =Value
In Kotlin, all data types are actually classes whose names start with a capital letter
 */
fun main(args:Array<String>){
    var a:Boolean =true
    var b:Char='R'
    var c:Byte=12
    var d:Short=356
    var e:Long=-4564687
    var f:Int=356
    var g:Float= 3.14f//f is important
    var h:Double=3.151592
    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
    println(h)


}
